DO $$
BEGIN
  IF EXISTS (
    SELECT 1
    FROM information_schema.columns
    WHERE table_name = 'presupuesto_lineas'
      AND column_name = 'id_linea'
      AND data_type = 'uuid'
  ) THEN
    DROP TABLE IF EXISTS presupuesto_lineas CASCADE;
  END IF;
END $$;

